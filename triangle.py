def area(a, b, c):
    '''Функция возвращает полупериметр треугольника.
	Параметры:
		a, b, c : 3 стороны треугольника
	Возвращаемое значение:
		(a + b + c) / 2 : полупериметр треугольника
    '''
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''Функция возвращает периметр треугольника.
	Параметры:
		a, b, c : 3 стороны треугольника
	Возвращаемое значение:
		a + b + c : периметр треугольника
    '''
    return a + b + c
