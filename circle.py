import math


def area(r):
    '''Функция возращает площадь круга.
	Параметры:
		r : радиус круга
	Возвращаемое значение:
		nath.pi * r * r : площадь круга
    '''
    return math.pi * r * r


def perimeter(r):
    '''Функция возвращает длину окружности.
	Параметры:
		r : радиус окружности
	Возвращаемое значение:
		2 * math.pi * r : длина окружности
    '''
    return 2 * math.pi * r
	
